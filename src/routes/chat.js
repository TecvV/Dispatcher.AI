router.get("/voice-calls/overview", async (req, res, next) => {
  try {
    const userId = req.user._id;
    const terminalStates = new Set(["completed", "rejected", "no_pickup", "cancelled", "failed"]);
    const activeStates = new Set(["queued", "dialing", "ringing", "in_progress", "streaming"]);

    // Reconcile latest active call status directly from Twilio (fast path, avoids stale UI).
    const latestActive = await VoiceRelayCall.findOne({
      userId,
      status: { $in: Array.from(activeStates) },
      $or: [{ terminalStatus: "" }, { terminalStatus: null }]
    })
      .sort({ updatedAt: -1, createdAt: -1 })
      .lean();
    if (latestActive?._id && latestActive?.callSid) {
      const twilioLive = await fetchTwilioCallStatus(latestActive.callSid);
      if (twilioLive?.ok && twilioLive.status) {
        const normalized = normalizeCallStatus(twilioLive.status);
        const isTerminal = terminalStates.has(normalized);
        await VoiceRelayCall.findByIdAndUpdate(latestActive._id, {
          $set: {
            status: normalized,
            terminalStatus: isTerminal ? normalized : String(latestActive.terminalStatus || "")
          },
          $push: {
            statusTimeline: {
              status: normalized,
              source: "twilio_poll",
              details: `Twilio polled status: ${String(twilioLive.status || "").trim() || "unknown"}`,
              at: new Date()
            }
          }
        });
        if (isTerminal) {
          await maybeExtractVoiceCallIntelligence(latestActive._id);
        }
      }
    }

    // Self-heal stale in-progress rows (e.g., missed stop callback) before returning overview.
    await VoiceRelayCall.updateMany(
      {
        userId,
        status: "in_progress",
        $or: [{ terminalStatus: "" }, { terminalStatus: null }],
        updatedAt: { $lt: new Date(Date.now() - 120000) }
      },
      {
        $set: { status: "completed", terminalStatus: "completed" },
        $push: {
          statusTimeline: {
            status: "completed",
            source: "overview_self_heal",
            details: "Stale in-progress call auto-closed.",
            at: new Date()
          }
        }
      }
    );

    const calls = await VoiceRelayCall.find({ userId })
      .sort({ updatedAt: -1, createdAt: -1 })
      .limit(20)
      .populate("contactId", "name")
      .lean();

    // If terminal call has empty insights (possible early extraction race), force one refresh now.
    const repairTarget = calls.find((c) => {
      const s = String(c?.terminalStatus || c?.status || "");
      const isTerminal = terminalStates.has(s);
      const hasCallerTurns = Array.isArray(c?.turns)
        ? c.turns.some((t) => String(t?.role || "") === "caller" && String(t?.text || "").trim())
        : false;
      const hasInsights =
        Boolean(String(c?.intelligenceSummary || "").trim()) ||
        (Array.isArray(c?.intelligenceKeyPoints) && c.intelligenceKeyPoints.length > 0);
      return isTerminal && hasCallerTurns && !hasInsights;
    });
    if (repairTarget?._id) {
      await maybeExtractVoiceCallIntelligence(repairTarget._id, { force: true });
      const refreshedCalls = await VoiceRelayCall.find({ userId })
        .sort({ updatedAt: -1, createdAt: -1 })
        .limit(20)
        .populate("contactId", "name")
        .lean();
      calls.length = 0;
      calls.push(...refreshedCalls);
    }

    const items = calls.map((c) => {
      const timeline = Array.isArray(c.statusTimeline)
        ? c.statusTimeline
            .map((t) => ({
              status: String(t?.status || ""),
              source: String(t?.source || ""),
              details: String(t?.details || ""),
              at: t?.at || null
            }))
            .filter((x) => x.status)
        : [];
      const rawStatus = String(c?.status || "queued");
      const rawTerminal = String(c?.terminalStatus || "");
      const timelineLatest = timeline.length ? String(timeline[timeline.length - 1]?.status || "") : "";
      const timelineLatestTerminal = terminalStates.has(timelineLatest) ? timelineLatest : "";
      const effectiveTerminal = terminalStates.has(rawTerminal)
        ? rawTerminal
        : timelineLatestTerminal || "";
      const effectiveStatus = effectiveTerminal || rawStatus || "queued";
      return {
        id: c._id,
        contactName: String(c?.contactId?.name || "Contact"),
        toNumber: String(c?.toNumber || ""),
        sentMessage: String(c?.message || ""),
        status: rawStatus,
        terminalStatus: rawTerminal,
        effectiveStatus,
        effectiveTerminalStatus: effectiveTerminal,
        createdAt: c?.createdAt || null,
        updatedAt: c?.updatedAt || null,
        intelligenceSummary: String(c?.intelligenceSummary || ""),
        intelligenceKeyPoints: Array.isArray(c?.intelligenceKeyPoints)
          ? c.intelligenceKeyPoints.map((x) => String(x || "").trim()).filter(Boolean)
          : [],
        intelligenceExtractedAt: c?.intelligenceExtractedAt || null,
        timeline
      };
    });

    const latest = items[0] || null;
    const latestCompletedWithIntel =
      items.find(
        (x) =>
          terminalStates.has(String(x.effectiveTerminalStatus || x.effectiveStatus || x.terminalStatus || x.status)) &&
          (x.intelligenceExtractedAt ||
            x.intelligenceSummary ||
            (Array.isArray(x.intelligenceKeyPoints) && x.intelligenceKeyPoints.length))
      ) || null;

    const intelLogs = items
      .filter((x) =>
        terminalStates.has(String(x.effectiveTerminalStatus || x.effectiveStatus || x.terminalStatus || x.status))
      )
      .map((x) => ({
        id: x.id,
        contactName: x.contactName,
        sentMessage: x.sentMessage,
        intelligenceSummary: x.intelligenceSummary,
        intelligenceKeyPoints: x.intelligenceKeyPoints,
        timestamp: x.intelligenceExtractedAt || x.updatedAt || x.createdAt,
        status: x.effectiveStatus || x.status
      }))
      .slice(0, 20);

    res.json({
      calls: items,
      latest,
      latestCompletedWithIntel,
      intelLogs
    });
  } catch (err) {
    next(err);
  }
});

