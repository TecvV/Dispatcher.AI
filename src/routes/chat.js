function normalizeAttachmentInputs(rawList = []) {
  if (!Array.isArray(rawList)) return [];
  const out = [];
  for (const raw of rawList.slice(0, 10)) {
    const fileName = String(raw?.fileName || raw?.name || "").trim();
    const mimeType = String(raw?.mimeType || raw?.type || "application/octet-stream").trim();
    const base64Raw = String(raw?.dataBase64 || raw?.base64 || raw?.data || "").trim();
    const dataUrl = String(raw?.dataUrl || "").trim();
    const payload = base64Raw || (dataUrl.includes(",") ? dataUrl.split(",")[1] : "");
    if (!fileName || !payload) continue;
    const safeBase64 = payload.replace(/\s/g, "");
    let content = null;
    try {
      content = Buffer.from(safeBase64, "base64");
    } catch {
      content = null;
    }
    if (!content || !content.length) continue;
    // 6 MB per file guard for API payload safety.
    if (content.length > 6 * 1024 * 1024) continue;
    out.push({ fileName, mimeType, content });
  }
  return out;
}

function appendAutoGeneratedNoReplyNote(text) {
  const base = String(text || "").trim();
  const note = "This is an auto-generated message, do not reply to this.";
  if (!base) return note;
  if (base.toLowerCase().includes(note.toLowerCase())) return base;
  return `${base}\n\n${note}`;
}

function appendAttachmentNames(text, attachments = []) {
  const base = String(text || "").trim();
  const names = Array.isArray(attachments)
    ? attachments
        .map((a) => String(a?.fileName || "").trim())
        .filter(Boolean)
    : [];
  if (!names.length) return base;
  const section = `Attachments: ${names.join(", ")}`;
  if (base.toLowerCase().includes("attachments:")) return base;
  return base ? `${base}\n\n${section}` : section;
}

function stripLeadingSalutation(text) {
  const t = String(text || "").trim();
  if (!t) return t;
  return t.replace(/^(dear|hello|hi|namaste)\s+[^,\n]{0,80},\s*/i, "").trim();
}

function emailSalutationForContact(contact) {
  const name = String(contact?.name || "").trim() || "there";
  const type = String(contact?.type || "").trim().toLowerCase();
  const isDoctor = type === "doctor" || type === "psychiatrist";
  if (isDoctor) {
    if (/^dr\.?\s+/i.test(name)) return `Dear ${name},`;
    return `Dear Dr. ${name},`;
  }
  return `Dear ${name},`;
}

function buildEmailBodyForContact(contact, commonBody) {
  const salutation = emailSalutationForContact(contact);
  const core = stripLeadingSalutation(commonBody);
  return `${salutation}\n\n${core}`;
}

